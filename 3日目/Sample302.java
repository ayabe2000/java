public class Sample302 {
    public static void main(String[] args){
        //標準出力
        int a = -5;
        System.out.println("a="+a);
        //入力した値が正の数か調べる
        if (a>0){
            System.out.println("aは正の数です"); //正の数だった場合実行
        }else{
            System.out.println("aは正の数ではありません"); //正の数ではなかった場合
        }

    }
}
